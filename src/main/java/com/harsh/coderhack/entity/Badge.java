package com.harsh.coderhack.entity;

/**
 * Represents the badges that a user can earn in the CoderHack application.
 */
public enum Badge {
     /**
     * Badge for users with scores between 1 and 30.
     */
    CODE_NINJA, 
    
    /**
     * Badge for users with scores between 30 and 60.
     */
    CODE_CHAMP, 
    
    /**
     * Badge for users with scores between 60 and 100.
     */
    CODE_MASTER
}
